    //function to calculate area
    private void getArea(int intL,int intW, int intH){
        double Area = (double)Math.round((2*(intH * intL) +2*(intH * intW))*100.0)/100.0;
        lblArea.setText(String.valueOf(Area));
        getTins(Area);
        
    }
    //function to calculate Number of tins
    private void getTins(double Area){
        int Tins = (int)Math.round(Area/10);
        lblTins.setText(String.valueOf(Tins));
        getTotalAmount(Tins);
    }
    //function to caculate total Cost/Amount
    private void getTotalAmount(int Tins){
        double Vat = (double)Math.round((30*Tins*0.15)*100.0)/100.0
                ,TotalAmount = (double)Math.round((Tins*30 +Vat)*100.0)/100.0;
        lblTotalCost.setText(String.valueOf(TotalAmount));
        lblVat.setText(String.valueOf(Vat));
    }
    
    //function for calculating change
    private void purchase(double TotalCost, double PayedAmount){
        double Change = 0.0;
        if(PayedAmount >= TotalCost){
            Change = PayedAmount-TotalCost;
        }else{
            Change = 0.0;
            JOptionPane.showMessageDialog(null,"Error_Insuficient Funds");
   
        }
        lblPayed.setText(String.valueOf(PayedAmount));
        lblChange.setText(String.valueOf(Change));
        
    }
    
