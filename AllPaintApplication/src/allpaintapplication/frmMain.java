    private void btnComputeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComputeActionPerformed
        // TODO add your handling code here:
        String strL = txtL.getText(),strW = txtW.getText(), strH = txtH.getText(); 
        int intL,intW,intH;
        
        if(strL == null&& strW == null && strH == null){
            JOptionPane.showMessageDialog(null, "Error_Empty Fields");
        }else{
            try{
                intL = Integer.parseInt(strL);
                intW = Integer.parseInt(strW);
                intH = Integer.parseInt(strH);
                getArea(intL,intW,intH);
                lblPayed.setText("0.0");
                lblChange.setText("0.0");
            }catch(Exception Ex){
                JOptionPane.showMessageDialog(null,"Error_Invalid Input Values");
            }
        }
    }//GEN-LAST:event_btnComputeActionPerformed

    private void txtPurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPurchaseActionPerformed
        // TODO add your handling code here:
        String strTotalCost = lblTotalCost.getText(),strPayedAmount = txtPayed.getText();                
        if("0.0".equals(strTotalCost)){
            JOptionPane.showMessageDialog(null, "Error_Product information not available");
        }else{
            try{
                double TotalCost = Double.parseDouble(strTotalCost);
                double PayedAmount = Double.parseDouble(strPayedAmount);
                purchase(TotalCost,PayedAmount);
            }catch(Exception Ex){
                JOptionPane.showMessageDialog(null,"Error_Invalid input type");
                lblPayed.setText("0.0");
                lblChange.setText("0.0");
                txtPayed.setText("");
            }
        }
    }//GEN-LAST:event_txtPurchaseActionPerformed

